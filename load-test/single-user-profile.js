/**
 * Single-user latency profiler for WeLovePDF.
 *
 * Purpose: figure out WHICH endpoint is the slow one, before bothering with
 * concurrent load tests. Hits each tool one request at a time through the
 * Next.js proxy (port 3000) — same path real users take — and reports min /
 * median / max latency across N iterations per endpoint.
 *
 * Run:
 *   node single-user-profile.js              # default 3 iterations, all endpoints
 *   node single-user-profile.js --iters 5    # 5 iterations each
 *   node single-user-profile.js --only pdf-to-jpg,compress-pdf
 *   node single-user-profile.js --base http://localhost:3000
 *
 * Endpoints needing custom test files (.docx, .xlsx, .pptx, scanned PDF):
 *   put them under load-test/samples/ and the script picks them up
 *   automatically. Missing ones are skipped (with a clear note) rather than
 *   blocking the whole run.
 *
 * Output: a sorted table — slowest first. That row IS your optimization
 * target. Median is the one to trust; max catches the worst-case cold start.
 */
const fs = require('node:fs');
const path = require('node:path');

// ── CLI args ──────────────────────────────────────────────────────────
function parseArgs() {
  const out = { iters: 3, base: 'http://localhost:3000', only: null, pdf: null };
  const argv = process.argv.slice(2);
  for (let i = 0; i < argv.length; i++) {
    const a = argv[i];
    if (a === '--iters') out.iters = parseInt(argv[++i], 10);
    else if (a === '--base') out.base = argv[++i].replace(/\/+$/, '');
    else if (a === '--only') out.only = argv[++i].split(',').map((s) => s.trim());
    else if (a === '--pdf') out.pdf = argv[++i];
    else if (a === '--help' || a === '-h') {
      console.log(
        'Usage: node single-user-profile.js [--iters N] [--base URL] [--only ep1,ep2] [--pdf path]',
      );
      process.exit(0);
    }
  }
  if (!Number.isFinite(out.iters) || out.iters < 1) {
    throw new Error('--iters must be a positive integer');
  }
  return out;
}

