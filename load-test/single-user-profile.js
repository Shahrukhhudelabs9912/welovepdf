// ── Timing helpers ────────────────────────────────────────────────────
function pct(arr, p) {
  if (arr.length === 0) return NaN;
  const sorted = [...arr].sort((a, b) => a - b);
  const idx = Math.min(sorted.length - 1, Math.floor((p / 100) * sorted.length));
  return sorted[idx];
}
function median(arr) {
  return pct(arr, 50);
}
function fmtMs(ms) {
  if (!Number.isFinite(ms)) return '   —';
  if (ms < 1000) return `${ms.toFixed(0).padStart(4)} ms`;
  return `${(ms / 1000).toFixed(2).padStart(4)} s `;
}

async function runOne(base, endpoint) {
  const url = `${base}/api/${endpoint.name}`;
  const body = endpoint.makeBody();
  if (body === null) {
    return { skipped: true, reason: `missing ${endpoint.requires || 'input'}` };
  }
  const t0 = performance.now();
  let res;
  try {
    res = await fetch(url, { method: 'POST', body });
  } catch (err) {
    return { error: `fetch failed: ${err.message}` };
  }
  // Drain the body — server-side latency includes streaming the full response.
  // Use arrayBuffer to be format-agnostic (PDF/JPG/ZIP/JSON all work).
  const buf = await res.arrayBuffer().catch(() => null);
  const elapsed = performance.now() - t0;
  return {
    ms: elapsed,
    status: res.status,
    ok: res.ok,
    bytes: buf ? buf.byteLength : 0,
  };
}

async function profile() {
  const { iters, base, only } = parseArgs();
  const endpoints = only
    ? ENDPOINTS.filter((e) => only.includes(e.name))
    : ENDPOINTS;
  if (endpoints.length === 0) {
    console.error('No endpoints matched --only filter.');
    process.exit(1);
  }

  console.log(`Profiling against ${base}  ·  ${iters} iter(s) per endpoint`);
  console.log(`${endpoints.length} endpoint(s), sequential (1 VU)\n`);

  const results = [];
  for (const ep of endpoints) {
    process.stdout.write(`  ${ep.name.padEnd(20)} `);
    const samples = [];
    let lastStatus = null;
    let lastError = null;
    let skipReason = null;

    for (let i = 0; i < iters; i++) {
      const r = await runOne(base, ep);
      if (r.skipped) {
        skipReason = r.reason;
        break;
      }
      if (r.error) {
        lastError = r.error;
        break;
      }
      lastStatus = r.status;
      if (r.ok) samples.push(r.ms);
      else {
        lastError = `HTTP ${r.status}`;
        break;
      }
      process.stdout.write('.');
    }

    if (skipReason) {
      console.log(`  SKIP (${skipReason})`);
      results.push({ name: ep.name, skipped: true, reason: skipReason });
      continue;
    }
    if (lastError) {
      console.log(`  FAIL (${lastError})`);
      results.push({ name: ep.name, failed: true, error: lastError });
      continue;
    }
    const min = Math.min(...samples);
    const med = median(samples);
    const max = Math.max(...samples);
    console.log(
      `  ok  min=${fmtMs(min)}  med=${fmtMs(med)}  max=${fmtMs(max)}`,
    );
    results.push({ name: ep.name, min, med, max, status: lastStatus });
  }

  // ── Final table, sorted slowest-first by median ────────────────────
  const ok = results.filter((r) => !r.skipped && !r.failed);
  ok.sort((a, b) => b.med - a.med);

  console.log('\n──── Slowest endpoints (sorted by median) ────');
  console.log(
    'endpoint              min        median     max'.padEnd(60),
  );
  console.log('─'.repeat(58));
  for (const r of ok) {
    console.log(
      `${r.name.padEnd(22)}${fmtMs(r.min)}  ${fmtMs(r.med)}  ${fmtMs(r.max)}`,
    );
  }

  const skipped = results.filter((r) => r.skipped);
  const failed = results.filter((r) => r.failed);
  if (skipped.length) {
    console.log('\n──── Skipped ────');
    for (const r of skipped) console.log(`  ${r.name}: ${r.reason}`);
  }
  if (failed.length) {
    console.log('\n──── Failed ────');
    for (const r of failed) console.log(`  ${r.name}: ${r.error}`);
  }

  // Hint: anything taking >1s on the test PDF is almost certainly a slow path
  // in the service layer (sync work in the event loop, missing thread-pool
  // offload, etc.) — flag those for the optimization pass.
  const slow = ok.filter((r) => r.med > 1000);
  if (slow.length) {
    console.log(
      `\n${slow.length} endpoint(s) >1s median — these are your top optimization targets:`,
    );
    for (const r of slow) console.log(`  · ${r.name}  (median ${fmtMs(r.med)})`);
  }
}

profile().catch((err) => {
  console.error('Profiler crashed:', err);
  process.exit(1);
});
